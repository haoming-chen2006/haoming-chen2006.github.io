export function createT(lang) {
  const dict = translations[lang] || translations.en;
  return function t(key, vars = {}) {
    let text = dict[key] ?? translations.en[key] ?? key;
    Object.entries(vars).forEach(([name, value]) => {
      text = text.replace(new RegExp(`\\{${name}\\}`, 'g'), String(value));
    });
    return text;
  };
}

export function roundLabel(round, t) {
  const map = {
    group: t('roundGroup'),
    r32: t('roundR32'),
    r16: t('roundR16'),
    qf: t('roundQf'),
    sf: t('roundSf'),
    third: t('roundThird'),
    final: t('roundFinal'),
  };
  return map[round] || round;
}

export function formatKickoff(value, lang) {
  const locale = lang === 'zh' ? 'zh-CN' : undefined;
  return new Intl.DateTimeFormat(locale, {
    weekday: 'short',
    month: 'short',
    day: 'numeric',
    hour: 'numeric',
    minute: '2-digit',
  }).format(new Date(value));
}

export function formatLockCountdown(deadlineIso, nowMs) {
  const ms = new Date(deadlineIso).getTime() - nowMs;
  if (ms <= 0) return null;
  const mins = Math.floor(ms / 60000);
  const secs = Math.floor((ms % 60000) / 1000);
  return `${mins}:${secs.toString().padStart(2, '0')}`;
}

export function getGuessDeadline(match) {
  return match.guess_deadline || match.kickoff_time;
}
